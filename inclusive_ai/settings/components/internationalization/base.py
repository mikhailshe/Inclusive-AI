from django.utils.translation import gettext_lazy as _

"""
Настройки интернационализации и локализации Django

Компонент отвечает за:
1. Настройки языков и переводов
2. Конфигурацию часовых поясов
3. Форматирование дат, чисел и валют
4. Пути к файлам переводов
"""

# ------------------ ОСНОВНЫЕ НАСТРОЙКИ ЯЗЫКОВ -------------------

# Языковой код по умолчанию
# Примеры: "en-us", "ru", "fr", "de", "es"
LANGUAGE_CODE = "ru"

# Список доступных языков в формате (код, название)
# Используется для формирования выбора языка в интерфейсе
LANGUAGES = [
    ("ru", _("Russian")),
    ("en", _("English")),
]

# Включить поддержку интернационализации
USE_I18N = True

# Включить локализацию форматирования дат и чисел
USE_L10N = True

# ------------------ НАСТРОЙКИ ЧАСОВЫХ ПОЯСОВ -------------------

# Часовой пояс проекта
# Примеры: "UTC", "Europe/Moscow", "America/New_York"
TIME_ZONE = 'Asia/Yekaterinburg'

# Использовать часовые пояса в датах/времени
USE_TZ = True

# ------------------ ФОРМАТИРОВАНИЕ -------------------

# Настройки форматирования дат
DATE_FORMAT = "d.m.Y"  # Пример: 31.12.2023
DATE_INPUT_FORMATS = [
    "%d.%m.%Y",  # "31.12.2023"
    "%d-%m-%Y",  # "31-12-2023"
    "%Y-%m-%d",  # "2023-12-31"
]

# Настройки форматирования времени
TIME_FORMAT = "H:i:s"  # Пример: 15:30:00
TIME_INPUT_FORMATS = [
    "%H:%M:%S",  # "15:30:00"
    "%H:%M",     # "15:30"
]

# Настройки форматирования дат и времени
DATETIME_FORMAT = "d.m.Y H:i:s"  # Пример: 31.12.2023 15:30:00
DATETIME_INPUT_FORMATS = [
    "%d.%m.%Y %H:%M:%S",  # "31.12.2023 15:30:00"
    "%d.%m.%Y %H:%M",     # "31.12.2023 15:30"
    "%Y-%m-%d %H:%M:%S",  # "2023-12-31 15:30:00"
]

